public class UnaryOperator{
    public static void main(String args[]){
        int a = 5;
        System.out.println(++a); // Output: 6
        System.out.println(a++); // Output: 6
        System.out.println(a);   // Output: 7
        int b = 10;
        System.out.println(--b); // Output: 9
        System.out.println(b--); // Output: 9
        System.out.println(b);   // Output: 8

    }
}